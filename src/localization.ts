export function getTranslatedStyle(style: string | undefined, lang: string): string {
  if (!style) return "";
  const stylesObj = LOCALIZATION[lang]?.travelStyles;
  if (stylesObj && stylesObj[style]) {
    return stylesObj[style].label;
  }
  return style;
}

export function getTranslatedInterest(interest: string | undefined, lang: string): string {
  if (!interest) return "";
  const interestsObj = LOCALIZATION[lang]?.interests;
  if (interestsObj && interestsObj[interest]) {
    // Remove emojis if we want pure text, or keep them. The dictionary has emojis so let's use it
    return interestsObj[interest];
  }
  return interest;
}

